def format_couple_moon_bridge(
    man_report: PartnerReport,
    woman_report: PartnerReport,
    *,
    include_transition_variants: bool = True,
) -> str:
    man = _data(man_report)
    woman = _data(woman_report)
    precision = legacy._pair_precision_note(man_report, woman_report)
    precision_text = f"\n\n🎯 Точность расчёта\n{precision}" if precision else ""
    alternate = legacy._alternate_moon_bridge_block(man_report, woman_report) if include_transition_variants else ""
    alternate_text = f"\n\n🎴 Если Луна могла сменить знак\n{alternate}" if alternate else ""
    message = _ready_message(man_report, woman_report)
    return f"""
💞 Эмоциональный мост: что происходит между вами на практике

Он: Луна в {legacy._sign_ru(man_report, 'moon')}, {legacy._element_ru(man_report, 'moon')}.
Вы: Луна в {legacy._sign_ru(woman_report, 'moon')}, {legacy._element_ru(woman_report, 'moon')}.{precision_text}

🧭 Коротко о вашей разнице
Ему проще оставаться в контакте, когда есть {man['works']}.
Вам проще оставаться в контакте, когда есть {woman['works']}.

Разница сама по себе не проблема. Проблема начинается, когда реакцию другого принимают за окончательный ответ на вопрос «любит или не любит».

🔄 Как раскручивается ссора
{_conflict_loop(man_report, woman_report)}

🗣 Что вы можете ошибочно прочитать
{_translation_lines(man_report, woman_report)}

🔬 Что добавляют точные знаки Луны
{_exact_sign_block(man_report, woman_report)}{alternate_text}

🌉 Что делать в реальном разговоре
1. Возьмите один эпизод, а не всю историю отношений: «вчера после разговора ты замолчал» вместо «ты всегда от меня закрываешься».
2. Дайте ему простой вход: {man['opening']}
3. Скажите свою часть без лекции и диагноза: {woman['own_need']}
4. Закончите одним вопросом или действием: {_joint_request(man_report, woman_report)}

💬 Три нормальные фразы

1. Короткое сообщение
«{message}»

2. Начало живого разговора
«Я не хочу снова ходить по кругу. Давай возьмём один вопрос: что сейчас между нами происходит и что мы делаем дальше?»

3. Когда разговор уже перегрелся
«{man['pause']}»

🚫 Что почти наверняка ухудшит ситуацию
С ним особенно не работает: {man['stop']}
Ваша типичная ловушка: {woman['self_trap']}.

✅ Как проверить, что это не просто красивый текст
{_success_criteria()}

🧪 Когда карта уже ни при чём
Одна неудачная попытка ничего не доказывает. Люди бывают уставшими, злыми и не особенно талантливыми в разговорах, сенсация века. Но если после двух-трёх прямых и спокойных попыток человек всё равно исчезает, обесценивает тему или оставляет всю работу вам, проблема не в Луне и не в неидеальной формулировке. Тогда важнее смотреть на взаимность и реальные поступки.

Главный смысл моста: не подобрать волшебный пароль, а быстрее понять, можете ли вы вдвоём превращать напряжение в ясный разговор и конкретное действие.
""".strip()
