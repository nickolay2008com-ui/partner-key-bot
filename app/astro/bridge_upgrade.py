def _element(report: PartnerReport) -> str:
    value = str(report.emotional_language or "earth")
    return value if value in ELEMENT_BRIDGE else "earth"


def _data(report: PartnerReport) -> dict[str, str]:
    return ELEMENT_BRIDGE[_element(report)]


def _pair_key(man_report: PartnerReport, woman_report: PartnerReport) -> tuple[str, str]:
    return tuple(sorted((_element(man_report), _element(woman_report))))  # type: ignore[return-value]


def _joint_request(man_report: PartnerReport, woman_report: PartnerReport) -> str:
    return PAIR_REQUESTS.get(
        _pair_key(man_report, woman_report),
        "Давай выберем одну небольшую ситуацию, спокойно назовём потребности обоих и договоримся об одном следующем шаге.",
    )


def _ready_message(man_report: PartnerReport, woman_report: PartnerReport) -> str:
    return f"{_data(man_report)['opening']} {_data(woman_report)['self']} {_joint_request(man_report, woman_report)}"


def _conflict_loop(man_report: PartnerReport, woman_report: PartnerReport) -> str:
    man = _data(man_report)
    woman = _data(woman_report)
    if _element(man_report) == _element(woman_report):
        return (
            f"Вы оба защищаете похожую потребность: {man['protects']}. Когда напряжение растёт, оба можете "
            f"реагировать одинаково: {man['stress']}. Поэтому один сценарий удваивается и кажется доказательством, "
            "что другой специально делает всё неправильно, хотя чаще оба пытаются вернуть себе безопасность одним способом."
        )
    return (
        f"Когда ему тревожно, он обычно {man['stress']}. Со стороны это может выглядеть как {man['outside']}. "
        f"Вы отвечаете своим способом: {woman['stress']}. Для него это может выглядеть как {woman['outside']}. "
        "Так возникает петля: каждый защищает важное, но второй слышит в этой защите отказ от близости."
    )


def _translation_lines(man_report: PartnerReport, woman_report: PartnerReport) -> str:
    man = _data(man_report)
    woman = _data(woman_report)
    return (
        f"• Когда он {man['stress']}, это не обязательно означает безразличие. Вероятнее, он защищает {man['protects']}.\n"
        f"• Когда вы {woman['stress']}, это не обязательно попытка усложнить отношения. Вы защищаете {woman['protects']}."
    )


