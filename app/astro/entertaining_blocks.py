def _pair_cast(man_report: PartnerReport, woman_report: PartnerReport, key: str) -> str:
    return (
        f"Он: {legacy._sign_ru(man_report, key)}, {legacy._element_ru(man_report, key)}.\n"
        f"Вы: {legacy._sign_ru(woman_report, key)}, {legacy._element_ru(woman_report, key)}."
    )


def _pair_experiment(man_report: PartnerReport, woman_report: PartnerReport, key: str) -> str:
    return (
        f"Сначала дайте ему понятный вход: {_action(man_report, key)}\n"
        f"Затем назовите свой формат одной фразой: {_action(woman_report, key)}\n"
        "Не выполняйте оба пункта как военную операцию. Достаточно одного естественного движения с каждой стороны."
    )


