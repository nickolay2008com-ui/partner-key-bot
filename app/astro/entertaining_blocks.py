def _element(report: PartnerReport, key: str) -> str:
    return legacy._element(report, key)


def _story(report: PartnerReport, key: str) -> str:
    return ELEMENT_STORIES[key].get(_element(report, key), ELEMENT_STORIES[key]["earth"])


def _action(report: PartnerReport, key: str) -> str:
    return ACTIONS[key].get(_element(report, key), ACTIONS[key]["earth"])


def _planet_line(report: PartnerReport, key: str) -> str:
    return (
        f"{PLANET_META[key]['title']} в {legacy._sign_ru_prepositional(report, key)}, "
        f"стихия {legacy._element_ru(report, key)}, {legacy._retrograde_label(report, key)}"
    )


def _precision_block(report: PartnerReport) -> str:
    note = legacy.format_moon_precision_note(report)
    return f"\n\n🎯 Насколько точен кадр\n{note}" if note else ""


def _planet_detail(report: PartnerReport, key: str, *, deep: bool = False) -> str:
    meta = PLANET_META[key]
    exact_detail = legacy._sign_detail(report, key)
    retrograde = legacy._retrograde_note(report, key, meta["title"])
    moon_extra = ""
    if key == "moon":
        moon_extra = (
            f"\n\n🎭 Как это выглядит без астрологических декораций\n"
            f"{legacy.format_moon_person_mechanic(legacy._placement(report, 'moon'), role='Его')}"
        )
    deep_extra = ""
    if deep:
        deep_extra = (
            "\n\n🕵️ Детектор реальности\n"
            "Проверьте три сцены: как он ведёт себя после тяжёлого дня, как принимает заботу и что делает, "
            "когда чувствует давление. Повторяющаяся реакция важнее самой красивой трактовки."
        )
    return f"""
{meta["emoji"]} {meta["title"]}: {meta["role"]}

Главный герой этого блока — {report.partner_name}.
{meta["hook"]}

🎬 Его версия сюжета
{_planet_line(report, key)}.

{_story(report, key)}{_precision_block(report) if key == "moon" else ""}{moon_extra}

🔍 Точный характер знака
{exact_detail}{retrograde}

✨ Его суперсила
{legacy._element_text(report, key)}

🚨 Когда сценарий ломается
{meta["danger"]}

🧪 Эксперимент на 24 часа
{_action(report, key)}

✅ Признак, что вы попали в его язык
{SIGNALS[key]}.{deep_extra}

Карта не выдаёт лицензию управлять человеком. Она даёт более умную гипотезу, чем привычное «ну он просто странный».
""".strip()


