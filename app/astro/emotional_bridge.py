def _placement(report: PartnerReport) -> dict[str, object]:
    value = report.placements.get("moon") if isinstance(report.placements, dict) else None
    return value if isinstance(value, dict) else {}


def _element(report: PartnerReport) -> str:
    moon = _placement(report)
    value = str(moon.get("element") or report.emotional_language or "earth")
    return value if value in ELEMENT_PROFILES else "earth"


def _sign(report: PartnerReport) -> str:
    sign = str(_placement(report).get("sign_ru") or "знак не определён")
    return SIGN_PREPOSITIONAL.get(sign, sign)


def _pair_key(man_report: PartnerReport, woman_report: PartnerReport) -> tuple[str, str]:
    return tuple(sorted((_element(man_report), _element(woman_report))))  # type: ignore[return-value]


def _shore(report: PartnerReport, *, role: str) -> dict[str, str]:
    element = _element(report)
    profile = ELEMENT_PROFILES[element]
    mechanic = moon_sign_mechanic(_placement(report))
    return {
        "role": role,
        "name": report.partner_name,
        "sign": f"Луна в {_sign(report)}",
        "element": profile["name"],
        "tagline": profile["tagline"],
        "need": mechanic["need"],
        "closes": mechanic["closed"],
        "bridge": mechanic["bridge"],
        "gift": profile["gift"],
        "signal": profile["signal"],
    }


