def _profile(report: PartnerReport, planet: str) -> dict[str, str]:
    element = _element(report, planet)
    data = ELEMENT_PROFILES[planet].get(element, ELEMENT_PROFILES[planet]["earth"])
    return {
        "name": report.partner_name,
        "sign": _sign_ru(report, planet),
        "element": _element_ru(report, planet),
        "motion": _retrograde_label(report, planet),
        "need": data["need"],
        "gift": data["gift"],
        "risk": data["risk"],
        "exact": _sign_detail(report, planet),
    }


def _pair_copy(man: dict[str, str], woman: dict[str, str]) -> tuple[str, str]:
    if man["element"] == woman["element"]:
        resource = (
            f"У вас общий базовый язык — {man['element']}. Поэтому многие реакции легче узнаются без перевода. "
            f"Сильная сторона пары: {man['gift']}."
        )
        friction = (
            "Похожесть не отменяет различий знаков. В слепой зоне оба могут одновременно использовать один и тот же "
            f"защитный сценарий: {man['risk']}."
        )
        return resource, friction
    resource = (
        f"Он приносит способность {man['gift']}. Вы приносите способность {woman['gift']}. "
        "Вместе это даёт паре больше возможностей, чем каждый стиль по отдельности."
    )
    friction = (
        f"Ему особенно нужны {man['need']}, а вам — {woman['need']}. Риск не в различии, а в том, что каждый "
        "предлагает другому собственный рабочий формат и не узнаёт встречную заботу."
    )
    return resource, friction


def _moon_layer(man_report: PartnerReport, woman_report: PartnerReport) -> dict[str, Any]:
    bridge = build_couple_moon_bridge_view(man_report, woman_report)
    shores = bridge["shores"]
    return {
        "key": "moon",
        "emoji": "🌙",
        "title": "Эмоциональная безопасность",
        "promise": "Как вы возвращаете друг другу ощущение близости",
        "formula": f"Его {shores[0]['sign']} · Ваша {shores[1]['sign']}",
        "resource": bridge["insight"]["body"],
        "friction": bridge["translation"]["body"],
        "sides": shores,
        "phrase": bridge["phrases"][0]["text"],
        "action": bridge["protocol"][2]["text"],
        "success": " ".join(bridge["experiment"]["success"]),
    }


def _planet_layer(
    man_report: PartnerReport,
    woman_report: PartnerReport,
    planet: str,
    emoji: str,
    title: str,
    promise: str,
) -> dict[str, Any]:
    man = _profile(man_report, planet)
    woman = _profile(woman_report, planet)
    resource, friction = _pair_copy(man, woman)
    action = PLANET_ACTIONS[planet]
    planet_name = PLANET_NAMES[planet]
    return {
        "key": planet,
        "emoji": emoji,
        "title": title,
        "promise": promise,
        "formula": (
            f"Его {planet_name}: {man['sign']} · {man['element']} · "
            f"Ваш{'а' if planet in {'venus'} else ''} {planet_name}: {woman['sign']} · {woman['element']}"
        ),
        "resource": resource,
        "friction": friction,
        "sides": [
            {"role": "Его стиль", **man},
            {"role": "Ваш стиль", **woman},
        ],
        "phrase": action["phrase"],
        "action": action["action"],
        "success": action["success"],
    }


