def _sign_key(item: dict[str, object]) -> str:
    return str(item.get("sign_key", ""))


def _sign_ru(item: dict[str, object]) -> str:
    return str(item.get("sign_ru", "не определён"))


def _element_ru(item: dict[str, object]) -> str:
    return str(item.get("element_ru", ""))


def _sign_ru_prepositional(item: dict[str, object]) -> str:
    sign = _sign_ru(item)
    return SIGN_PREPOSITIONAL.get(sign, sign)


def _mechanic(item: dict[str, object]) -> dict[str, str]:
    return MOON_SIGN_MECHANICS.get(
        _sign_key(item),
        {
            "need": "свой формат спокойствия, близости и эмоционального восстановления",
            "closed": "напор, спешка и попытка объяснить человека только одной стихией",
            "bridge": "сначала уточнить, что человеку реально помогает расслабиться и быть ближе",
        },
    )


def format_moon_person_mechanic(item: dict[str, object], *, role: str) -> str:
    """Precise Moon-in-sign explanation for one person."""
    mechanic = _mechanic(item)
    peace_label = "Ваш внутренний покой" if role == "Ваша" else f"{role} внутренний покой"
    return (
        f"{peace_label} включается через {mechanic['need']}.\n"
        f"Защита может включаться, если вокруг есть {mechanic['closed']}."
    )


def _pair_dynamic(man_moon: dict[str, object], woman_moon: dict[str, object]) -> str:
    man_sign = _sign_key(man_moon)
    woman_sign = _sign_key(woman_moon)
    man_element = str(man_moon.get("element", ""))
    woman_element = str(woman_moon.get("element", ""))

    if man_sign and man_sign == woman_sign:
        return (
            "У вас похожий эмоциональный код: легче быстрее узнавать друг друга, "
            "но выше риск одновременно закрываться одинаковым способом. Главная задача — не доказывать, "
            "кто чувствует правильнее, а заранее договориться о паузе и мягком возвращении в контакт."
        )
    if man_element and man_element == woman_element:
        return (
            "У вас близкая стихия Луны: базовый ритм может считываться легче, чем у пар с разными стихиями. "
            "Нюанс в знаках: потребность похожа по направлению, но конкретный способ поддержки может отличаться."
        )

    return (
        "У вас разные эмоциональные стихии: это не минус, а зона перевода. "
        "Один может ждать свой формат безопасности, пока второй даёт любовь на своём языке. "
        "Мост появляется, когда вы сначала называете нужный формат, а не проверяете чувства через догадки."
    )


def _pair_body(man_moon: dict[str, object], woman_moon: dict[str, object], *, include_basis: bool) -> str:
    man = _mechanic(man_moon)
    woman = _mechanic(woman_moon)
    man_label = f"его Луна в {_sign_ru_prepositional(man_moon)}, {_element_ru(man_moon)}"
    woman_label = f"ваша Луна в {_sign_ru_prepositional(woman_moon)}, {_element_ru(woman_moon)}"
    basis = f"Основание:\n{man_label}; {woman_label}.\n\n" if include_basis else f"{man_label}; {woman_label}.\n\n"
    return f"""
{basis}Коротко о мосте:
{_pair_dynamic(man_moon, woman_moon)}

Эмоциональный мост — это не магическая совместимость и не попытка стать удобной. Это переводчик между двумя нервными системами: где ему безопасно приближаться, где вам тепло оставаться собой и какой формат разговора не превращает любовь в экзамен.

1. Его точка спокойствия:
{format_moon_person_mechanic(man_moon, role="Его")}

Что это значит по-женски практично: сначала не требовать доказательства чувства, а дать вход в контакт в его ритме. Когда мужчина получает знакомый сигнал безопасности, у него меньше причин защищаться и больше шансов услышать вас живым человеком, а не претензией.

2. Ваша точка тепла:
{format_moon_person_mechanic(woman_moon, role="Ваша")}

Ваш ритм тоже важен. Мост не работает, если вы всё время переводите только его язык и теряете свой. Ваша задача — не молчать «ради гармонии», а назвать свой способ тепла так, чтобы это звучало как приглашение, а не приговор.

3. Где может ломаться контакт:
Он может искать {man["need"]}, а вы — {woman["need"]}.
Его может закрывать: {man["closed"]}.
Вас может закрывать: {woman["closed"]}.
Если каждый даёт другому именно то, от чего тот закрывается, контакт может ломаться даже при наличии чувств. Это тот самый момент, когда люди любят, но разговаривают так, будто нажимают друг другу не те кнопки.

4. Главная развилка пары:
не спорить, кто чувствует правильнее, а увидеть две разные инструкции к безопасности. Ему важно получить свой сигнал спокойствия, вам — свой сигнал тепла. Когда оба сигнала названы, разговор перестаёт быть угадайкой и становится маршрутом к близости: сначала атмосфера, потом смысл, затем маленькое действие.

5. Точный мост:
Для него работает: {man["bridge"]}.
Для вас работает: {woman["bridge"]}.

Как открыть разговор без давления:
1. Начните с его формата безопасности: темп, тон, пространство или конкретика.
2. Назовите одно своё чувство без обвинения: «мне теплее, когда…», «мне спокойнее, если…».
3. Попросите не глобальное обещание, а маленький следующий шаг: ответить, обнять, договориться о времени, написать позже или просто услышать.

Фраза-мост:
«Я не хочу угадывать за нас двоих и устраивать допрос с романтическим освещением. Давай попробуем проще: я учитываю, что помогает расслабиться тебе, а ты услышишь, что делает теплее мне. Нам не нужно решить всю судьбу отношений за один вечер — достаточно одного честного шага навстречу».

Если он закрывается:
снижайте не ценность своих чувств, а интенсивность входа. Иногда мост ломается не потому, что вы «не подходите», а потому что тон, скорость или объём разговора попали в его защиту. Вернитесь к короткой фразе, паузе и конкретному вопросу.

Если вы закрываетесь:
не превращайте заботу о его ритме в отказ от себя. Скажите мягко, но ясно, какой формат бережности нужен вам. Хороший мост держится на двух берегах: его спокойствии и вашем тепле.

Мини-проверка реальностью:
если после такой формы ему спокойнее, вам теплее, а в контакте меньше обороны и больше ясного ответа — мост выбран верно. Если становится тяжелее, меняйте не чувства, а форму входа: темп, тон, конкретику или количество пространства.
""".strip()


def format_moon_pair_mechanic(man_moon: dict[str, object], woman_moon: dict[str, object]) -> str:
    """Sign-first bridge for two Moon placements."""
    return _pair_body(man_moon, woman_moon, include_basis=True)


def format_moon_variant_pair(man_moon: dict[str, object], woman_moon: dict[str, object]) -> str:
    return _pair_body(man_moon, woman_moon, include_basis=False)
