def _sign_key(item: dict[str, object]) -> str:
    return str(item.get("sign_key", ""))


def _sign_ru(item: dict[str, object]) -> str:
    return str(item.get("sign_ru", "не определён"))


def _element_ru(item: dict[str, object]) -> str:
    return str(item.get("element_ru", ""))


def _mechanic(item: dict[str, object]) -> dict[str, str]:
    return MOON_SIGN_MECHANICS.get(
        _sign_key(item),
        {
            "need": "свой формат спокойствия, близости и эмоционального восстановления",
            "closed": "давление, спешка и попытка объяснить человека только одной стихией",
            "bridge": "сначала уточнить, что человеку реально помогает расслабиться и быть ближе",
        },
    )


def format_moon_person_mechanic(item: dict[str, object], *, role: str) -> str:
    """Precise Moon-in-sign explanation for one person."""
    mechanic = _mechanic(item)
    return (
        f"{role} Луна в {_sign_ru(item)}, стихия {_element_ru(item)}.\n"
        f"Спокойствие включается через: {mechanic['need']}.\n"
        f"Закрываться может от: {mechanic['closed']}."
    )


def _pair_body(man_moon: dict[str, object], woman_moon: dict[str, object], *, include_basis: bool) -> str:
    man = _mechanic(man_moon)
    woman = _mechanic(woman_moon)
    man_label = f"его Луна в {_sign_ru(man_moon)}, {_element_ru(man_moon)}"
    woman_label = f"ваша Луна в {_sign_ru(woman_moon)}, {_element_ru(woman_moon)}"
    basis = f"Основание:\n{man_label}; {woman_label}.\n\n" if include_basis else f"{man_label}; {woman_label}.\n\n"
    return f"""
{basis}Где ему спокойнее:
{format_moon_person_mechanic(man_moon, role="Его")}

Где вам теплее:
{format_moon_person_mechanic(woman_moon, role="Ваша")}

Где можно не понять друг друга:
Он может искать {man['need']}, а вы — {woman['need']}.
Его может закрывать: {man['closed']}.
Вас может закрывать: {woman['closed']}.
Если каждый даёт другому именно то, от чего тот закрывается, контакт может ломаться даже при наличии чувств.

Точный мост:
Для него работает: {man['bridge']}.
Для вас работает: {woman['bridge']}.

Практичный шаг:
Сначала дайте человеку его формат безопасности, потом добавьте свой формат контакта. Не начинать с требования быть другим, а собрать маленький общий шаг: один тёплый сигнал, один ясный вопрос, одна простая договорённость.
""".strip()


def format_moon_pair_mechanic(man_moon: dict[str, object], woman_moon: dict[str, object]) -> str:
    """Sign-first bridge for two Moon placements."""
    return _pair_body(man_moon, woman_moon, include_basis=True)


def format_moon_variant_pair(man_moon: dict[str, object], woman_moon: dict[str, object]) -> str:
    return _pair_body(man_moon, woman_moon, include_basis=False)
